    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //Pass execution on to doPost
        doPost(request,response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
       
        //get the form data and set up a Movie object
        int movieID = Integer.parseInt(request.getParameter("movieID"));
        String movieTitle = request.getParameter("movieTitle");
        String movieGenre = request.getParameter("movieGenre");
        int movieYear = Integer.parseInt(request.getParameter("movieYear"));
        String movieDirector = request.getParameter("movieDirector");
        
        Movies movie = new Movies();
        movie.setMovieID(movieID);
        movie.setMovieTitle(movieTitle);
        movie.setMovieGenre(movieGenre);
        movie.setMovieYear(movieYear);
        movie.setMovieDirector(movieDirector);
        
        //create an UpdateQuery object and use it to update the movie
        UpdateQuery uq = new UpdateQuery();
        uq.doUpdate(movie);
        
        //pass control to the ReadServlet
        String url = "/read";
        
        RequestDispatcher dispatcher = request.getRequestDispatcher(url);
        dispatcher.forward(request,response);
        
        
        
        
    }

